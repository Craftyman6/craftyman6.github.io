//
//Weapon object used to represent a weapon
//
export class Weapon {
    //constructor fille with array of weapon name, sub name, special name,
    //and weapon class name
    constructor(arr) {
        this.name = arr.name;
        this.sub = new Sub(arr.sub);
        this.special = new Special(arr.special);
        this.wclass = new WClass(arr.wclass);
        this.imgSrc = "img/Main Weapons/"+arr.name+".png";
        this.imgHTML = "<img src=\""+this.imgSrc+"\" onclick=\"weaponClick("+arr.id+")\" title=\""+this.name+"\">";
    }

	static allWeapons = weapons.map(weapon => new Weapon(weapon));

	//returns a list of weapons that have the given parameter
	//Can take one parameter to search for a weapon of given sub, special or wclass
	//Can take two parameters to serch for a weapon of given sub and special
	static getWeaponsOf() {
		if (arguments.length === 1) {
			const search = arguments[0];
			let found = [];
			for (const weapon of Weapon.allWeapons) {
				if (search instanceof Sub) {
					if (weapon.getSub().equals(search)) {
						found.push(weapon);
					}
				} else if (search instanceof Special) {
					if (weapon.getSpecial().equals(search)) {
						found.push(weapon);
					}
				} else if (search instanceof WClass) {
					if (weapon.getWClass().equals(search)) {
						found.push(weapon);
					}
				}
			}
			return found;
		} else if (arguments.length === 2) {
			const sub = arguments[0];
			const special = arguments[1];
			let found = [];
			for (const weapon of Weapon.getWeaponsOf(sub)) {
				if (weapon.getSpecial().equals(special)) {
					found.push(weapon);
				}
			}
			return found;
		} else {return []}
	}

	//returns a random weapon from all the weapons
	static getRandomWeapon() {
		return Weapon.allWeapons[Math.floor(Math.random() * Weapon.allWeapons.length)];
	}

	//returns a random weapon from a given array of weapons
	static getRandomWeaponOf(arr) {
		return arr[Math.floor(Math.random() * arr.length)];
	}

    //get name function
    getName() {return this.name;}

    //get sub function
    getSub() {return this.sub;}

    //get special function
    getSpecial() {return this.special;}

    //get weapon class function
    getWClass() {return this.wclass;}

    //get imgSrc function
    getImgSrc() {return this.imgSrc;}

    //get imgHTML function
    getImgHTML() {return this.imgHTML;}

    //equals function the returns whether a weapon object is
    //equal to this one
    equals(weapon) {return weapon.getName() == this.name;}

	//returns whether a weapon object has a given sub or special
	has() {
		const search = arguments[0];
		if (search instanceof Sub) {
			return this.sub.equals(search);
		} else if (search instanceof Special) {
			return this.special.equals(search);
		} else {return false;}
	}

	//takes either a weapon or both a sub and a special
	//tests if the weapon's kit matches the provided kit
	sameKit() {
		if (arguments.length === 1) {
			const weapon = arguments[0];
			return weapon.getSub().equals(this.sub) &&
			weapon.getSpecial().equals(this.special);
		} else if (arguments.length === 2) {
			const sub = arguments[0];
			const special = arguments[1];
			return sub.equals(this.sub) &&
			special.equals(this.special);
		}
	}

	//returns an array of weapon objects that have the same kit as this weapon
	getWeaponsOfDupeKit() {
		let ret = [];
		Weapon.allWeapons.forEach( weapon => {
			if (weapon.sameKit(this) && !weapon.equals(this)) {
				ret.push(weapon);
			}
		})
		return ret;
	}
}

//function that fills a div element with images of given splatoon objects
export function renderListItems(containerId, items) {
    const container = document.getElementById(containerId);
    if (!container) {
        console.error(`Container with ID "${containerId}" not found.`);
        return;
    }

    // Clear the container to prevent duplication
    container.innerHTML = '';

    // Add each item as a child to the container
    items.forEach(item => {
        const listItem = document.createElement('div');
        listItem.innerHTML = item.getImgHTML();
        container.appendChild(listItem);
    });

    //Make sure list is displayed
    document.getElementById(containerId).style.display="inline-flex";
}

